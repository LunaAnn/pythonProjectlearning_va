"""
len() : 字符串的长路
format: 格式化输出(进阶)

    1.格式化小数长度(会四舍五入)  :.2f
    2.讲小数按百分比的形式显示    :.2%
    s1 = 3.6567

"""

s = "python"
print(len(s))

s1 = 3.6567
x  = 5.6567
print("今天的橘子{:.2f}一斤".format(s1, x)) #得到的是3.66
print('今天的橘子{1:.2f}一斤'.format(s1, x)) #看对应的下标

print('百分比为{:.2%}'.format(0.23455)) #2是百分比小数位的位数,
print('百分比为{:.3f}'.format(x))