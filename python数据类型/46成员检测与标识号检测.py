"""
成员检测与标识号检测:
    使用"in"和"not in"运算符来判断某个对象是否为序列成员
    in : 判断对象是否在序列(列表/字符串/元组/字典)中, 如果是则返回True
    not in : 判断对象是否不在序列中, 如果是则返回True
"""
print("4" in "1234") #字符串4是否存在于 字符串"1234"当中
print(1 in (1, 2, 3, 4))
print(1 in [1, 2, 3])
print("名字" in {"名字" : "美女"})
print("美女" in {"名字" : "美女"}) #返回false 因为判断的是键

# not in   不存在
print(3 not in (1, 2, 9))
