"""
    列表的切片取值  *取值取左不取右

"""

list_1 = [10, 20, 30, 40, 50, 60, 70]
print(list_1[0:3])              #[10, 20, 30]
print(list_1[-3:-1])            #[50, 60]
print(list_1[-3:])              #[50, 60, 70]
print(list_1[:])                #[10, 20, 30, 40, 50, 60, 70]
print(list_1[1:6:2])            #[20, 40, 60]
print(list_1[-6:-1:2])          #[20, 40, 60]
print(list_1[::-1])