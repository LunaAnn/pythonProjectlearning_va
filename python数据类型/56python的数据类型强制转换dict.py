"""
python的数据类型强制转换:
    dict() 其他转字典类型
        1. 数字类型是非容器类型, 不能转换为字典
        2. 字符串不能转字典类型, 因为字符串不能生成二级容器
        3. 列表类型转字典类型, 列表必须为等长二级容器, 子容器中的元素个数必须为2
        4. 元组类型转字典类型, 列表必须为等长二级容器, 子容器中的元素个数必须为2集
        5. 集合不能转字典类型
"""

# a = 123 #数字的类型不能转换为字典
