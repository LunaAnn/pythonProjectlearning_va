c = [1, 2, 3, "我", "a"]
cc = [[1, 2], ['a', 'b'], ['名字', '美女']] #{1: 2, 'a': 'b', '名字': '美女'}
print(dict(cc))

d = (1, 2, '我爱你', 6, "abc")
dd = ((1,2), ('a', 'b'), ('名字', '美女')) #{1: 2, 'a': 'b', '名字': '美女'}
print(dict(dd))

e = {1,2, '我爱你', 6, "abc"}
ee = {(1, 2), ('a', 'b'), ('名字', '美女')} #集合内只有是元组的时候才能转换 列表和集合都不可以
print(dict(ee))